    private void combDniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combDniActionPerformed
        if (!cargado) {
            return;
        }
        int index = combDni.getSelectedIndex();
        if (radAlum.isSelected()) {
            Alumno alum = listaAlum.get(index);
            rellenarTodoAlumno(alum);
        }else{
            Docente doc = listaDoc.get(index);
            rellenarTodoDocente(doc);
        }
    }//GEN-LAST:event_combDniActionPerformed

    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        int respuesta = JOptionPane.showConfirmDialog(this, "¬øEstas seguro de eliminar este " + (radAlum.isSelected() ? "Alumno?" : "Docente?"),"Confirmar eliminaci√≥n",JOptionPane.YES_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            if (radAlum.isSelected()) {
                ArchivoJson.eliminarAlumno(archivo, (String) combDni.getSelectedItem());
                JOptionPane.showMessageDialog(this, "Alumno eliminado correctamente");
            }else{
                ArchivoJson.eliminarDocente(archivo, (String) combDni.getSelectedItem());
                JOptionPane.showMessageDialog(this, "Docente eliminado correctamente");
            }
            if(radDocen.isSelected()){
                rellenarDniDocentes();
            }else{
                rellenarDniAlumnos();
            }
        }
    }//GEN-LAST:event_btnModActionPerformed
    

    
    public void rellenarDniAlumnos(){
        cargado = false;
        combDni.removeAllItems();
        listaAlum = ArchivoJson.leerAlumnos(archivo);
        Alumno primerAlum = listaAlum.get(0);
        rellenarTodoAlumno(primerAlum);
        for(Alumno alum : listaAlum){
            combDni.addItem(alum.getDni());
        }
        cargado = true;
    }
    public void rellenarDniDocentes(){
        cargado = false;
        combDni.removeAllItems();
        listaDoc = ArchivoJson.leerDocentes(archivo);
        Docente primerDoc = listaDoc.get(0);
        rellenarTodoDocente(primerDoc);
        for(Docente doc : listaDoc){
            combDni.addItem(doc.getDni());
        }
        cargado = true;
    }
    public void rellenarTodoAlumno(Alumno alum){
        limpiar();
        labelApeNom.setText(alum.getApeNom());
        labelFechaNac.setText(alum.getFechaNac());
        labelTelef.setText(alum.getTelef());
        labelDirec.setText(alum.getDireccion());
        labelSexo.setText(alum.getSexo());
    }
    public void rellenarTodoDocente(Docente doc){
        limpiar();
        labelApeNom.setText(doc.getApeNom());
        labelFechaNac.setText(doc.getFechaNac());
        labelTelef.setText(doc.getTelef());
        labelDirec.setText(doc.getDireccion());
        labelProf.setText(doc.getProfesion());
        labelSexo.setText(doc.getSexo());
    }
    
    public void limpiar(){
        labelApeNom.setText("");
        labelFechaNac.setText("");
        labelTelef.setText("");
        labelDirec.setText("");
        labelProf.setText("");
        labelSexo.setText("");
    }
    
